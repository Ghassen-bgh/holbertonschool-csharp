using System;

namespace MyMath
{
    /// <summary>
    /// Mathematical operations.
    /// </summary>
    class Operations
    {
        /// <summary>
        /// Add two integers.
        /// </summary>
        /// <param name="a">First integer.</param>
        /// <param name="b">Second integer.</param>
        /// <returns>Sum of the two integers.</returns>
        public static int Add(int a, int b)
        {
            return a + b;
        }
    }
}
